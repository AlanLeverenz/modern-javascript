// Const

const person = {
  name: 'John',
  age: 30,
};

// can mutate but not reassign a const
person.name = 'Sara';
person.age = 42;

console.log(person);

const numbers = [1, 2, 3, 4, 5];
numbers.push(6);
console.log(numbers);

